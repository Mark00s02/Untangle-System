    private void bsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsearchActionPerformed
        String searchQuery = recordsearch.getText();
        
        Highlighter highlighter = Records.getHighlighter();
        HighlightPainter painter = new DefaultHighlighter.DefaultHighlightPainter(Color.yellow);
        
        int startIndex = Records.getText().indexOf(searchQuery);
        int endIndex = startIndex + searchQuery.length();

        
        try {
            highlighter.addHighlight(startIndex, endIndex, painter);
            
        } catch (BadLocationException ex) {
        }
        
    }//GEN-LAST:event_bsearchActionPerformed

