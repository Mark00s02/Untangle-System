    private void bimportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bimportActionPerformed
        
        try {
            records1 = new FileReader("PatientRecords.txt");
            
            records1.read(record);
           
        
        } catch (IOException ex) {
            Logger.getLogger(Admin_Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Records.append(String.valueOf(record));
        
        
        try {
            records1.close();
      
        } catch (IOException ex) {
            Logger.getLogger(Admin_Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Records.setEnabled(true);
        
        
    }//GEN-LAST:event_bimportActionPerformed

